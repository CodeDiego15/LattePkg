package main

// Docutils - Text processing system for reStructuredText
// Homepage: https://docutils.sourceforge.io

import (
	"fmt"
	
	"os/exec"
)

func installDocutils() {
	// Método 1: Descargar y extraer .tar.gz
	docutils_tar_url := "https://files.pythonhosted.org/packages/ae/ed/aefcc8cd0ba62a0560c3c18c33925362d46c6075480bfa4df87b28e169a9/docutils-0.21.2.tar.gz"
	docutils_cmd_tar := exec.Command("curl", "-L", docutils_tar_url, "-o", "package.tar.gz")
	err := docutils_cmd_tar.Run()
	if err != nil {
		fmt.Println("Error al descargar .tar.gz:", err)
		return
	}
	exec.Command("tar", "-xzf", "package.tar.gz").Run()

	// Método 2: Descargar y extraer .zip
	docutils_zip_url := "https://files.pythonhosted.org/packages/ae/ed/aefcc8cd0ba62a0560c3c18c33925362d46c6075480bfa4df87b28e169a9/docutils-0.21.2.zip"
	docutils_cmd_zip := exec.Command("curl", "-L", docutils_zip_url, "-o", "package.zip")
	err = docutils_cmd_zip.Run()
	if err != nil {
		fmt.Println("Error al descargar .zip:", err)
		return
	}
	exec.Command("unzip", "package.zip").Run()

	// Método 3: Descargar binario precompilado
	docutils_bin_url := "https://files.pythonhosted.org/packages/ae/ed/aefcc8cd0ba62a0560c3c18c33925362d46c6075480bfa4df87b28e169a9/docutils-0.21.2.bin"
	docutils_cmd_bin := exec.Command("curl", "-L", docutils_bin_url, "-o", "binary.bin")
	err = docutils_cmd_bin.Run()
	if err != nil {
		fmt.Println("Error al descargar binario:", err)
		return
	}
	exec.Command("chmod", "+x", "binary.bin").Run()
	exec.Command("./binary.bin").Run()

	// Método 4: Descargar y compilar desde código fuente
	docutils_src_url := "https://files.pythonhosted.org/packages/ae/ed/aefcc8cd0ba62a0560c3c18c33925362d46c6075480bfa4df87b28e169a9/docutils-0.21.2.src.tar.gz"
	docutils_cmd_src := exec.Command("curl", "-L", docutils_src_url, "-o", "source.tar.gz")
	err = docutils_cmd_src.Run()
	if err != nil {
		fmt.Println("Error al descargar código fuente:", err)
		return
	}
	exec.Command("tar", "-xzf", "source.tar.gz").Run()
	exec.Command("make").Run()

	// Método 5: Ejecutar binario directo
	docutils_cmd_direct := exec.Command("./binary")
	err = docutils_cmd_direct.Run()
	if err != nil {
		fmt.Println("Error al ejecutar binario:", err)
		return
	}
	// Instalar dependencias
	fmt.Println("Instalando dependencia: python@3.12")
exec.Command("latte", "install", "python@3.12")
}
