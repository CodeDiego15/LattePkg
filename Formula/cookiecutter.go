package main

// Cookiecutter - Utility that creates projects from templates
// Homepage: https://github.com/cookiecutter/cookiecutter

import (
	"fmt"
	
	"os/exec"
)

func installCookiecutter() {
	// Método 1: Descargar y extraer .tar.gz
	cookiecutter_tar_url := "https://files.pythonhosted.org/packages/52/17/9f2cd228eb949a91915acd38d3eecdc9d8893dde353b603f0db7e9f6be55/cookiecutter-2.6.0.tar.gz"
	cookiecutter_cmd_tar := exec.Command("curl", "-L", cookiecutter_tar_url, "-o", "package.tar.gz")
	err := cookiecutter_cmd_tar.Run()
	if err != nil {
		fmt.Println("Error al descargar .tar.gz:", err)
		return
	}
	exec.Command("tar", "-xzf", "package.tar.gz").Run()

	// Método 2: Descargar y extraer .zip
	cookiecutter_zip_url := "https://files.pythonhosted.org/packages/52/17/9f2cd228eb949a91915acd38d3eecdc9d8893dde353b603f0db7e9f6be55/cookiecutter-2.6.0.zip"
	cookiecutter_cmd_zip := exec.Command("curl", "-L", cookiecutter_zip_url, "-o", "package.zip")
	err = cookiecutter_cmd_zip.Run()
	if err != nil {
		fmt.Println("Error al descargar .zip:", err)
		return
	}
	exec.Command("unzip", "package.zip").Run()

	// Método 3: Descargar binario precompilado
	cookiecutter_bin_url := "https://files.pythonhosted.org/packages/52/17/9f2cd228eb949a91915acd38d3eecdc9d8893dde353b603f0db7e9f6be55/cookiecutter-2.6.0.bin"
	cookiecutter_cmd_bin := exec.Command("curl", "-L", cookiecutter_bin_url, "-o", "binary.bin")
	err = cookiecutter_cmd_bin.Run()
	if err != nil {
		fmt.Println("Error al descargar binario:", err)
		return
	}
	exec.Command("chmod", "+x", "binary.bin").Run()
	exec.Command("./binary.bin").Run()

	// Método 4: Descargar y compilar desde código fuente
	cookiecutter_src_url := "https://files.pythonhosted.org/packages/52/17/9f2cd228eb949a91915acd38d3eecdc9d8893dde353b603f0db7e9f6be55/cookiecutter-2.6.0.src.tar.gz"
	cookiecutter_cmd_src := exec.Command("curl", "-L", cookiecutter_src_url, "-o", "source.tar.gz")
	err = cookiecutter_cmd_src.Run()
	if err != nil {
		fmt.Println("Error al descargar código fuente:", err)
		return
	}
	exec.Command("tar", "-xzf", "source.tar.gz").Run()
	exec.Command("make").Run()

	// Método 5: Ejecutar binario directo
	cookiecutter_cmd_direct := exec.Command("./binary")
	err = cookiecutter_cmd_direct.Run()
	if err != nil {
		fmt.Println("Error al ejecutar binario:", err)
		return
	}
	// Instalar dependencias
	fmt.Println("Instalando dependencia: certifi")
	exec.Command("latte", "install", "certifi").Run()
	fmt.Println("Instalando dependencia: libyaml")
	exec.Command("latte", "install", "libyaml").Run()
	fmt.Println("Instalando dependencia: python@3.12")
	exec.Command("latte", "install", "python@3.12").Run()
}
